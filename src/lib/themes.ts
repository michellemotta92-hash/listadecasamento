export function getThemeById(id: string): Theme {
  return themes.find(t => t.id === id) || themes[0];
}

export function applyTheme(themeId: string) {
  const theme = getThemeById(themeId);
  const root = document.documentElement;

  // Remove previous theme vars
  root.removeAttribute('style');

  if (theme.id === 'default') return;

  // Apply CSS variables
  for (const [key, value] of Object.entries(theme.vars)) {
    root.style.setProperty(key, value);
  }

  // Apply background and text colors
  if (theme.vars['--theme-bg']) {
    document.body.style.backgroundColor = theme.vars['--theme-bg'];
  }
  if (theme.vars['--theme-text']) {
    document.body.style.color = theme.vars['--theme-text'];
  }
}
